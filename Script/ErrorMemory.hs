showDtc :: (Int,[Word8]) -> String
showDtc (n,[a,b,c,status]) = show n ++ ".)" ++ showAsHexOneString [a,b,c] ++ " (" ++ showAsHex status ++ ")"

split4s :: [Word8] -> [[Word8]]
split4s [] = []
split4s (a:b:c:d:xs) = [a,b,c,d]:split4s xs

